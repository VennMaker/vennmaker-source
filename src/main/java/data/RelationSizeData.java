package data;

import java.util.Map;
import java.util.Vector;

/**
 * Objects of this class contains the sizes and values defnied in
 * <code>CDialogRelationSize</code>
 * 
 * 
 * 
 */
public class RelationSizeData
{
	/**
	 * The definied sizes
	 */
	private Map<String, Vector<Integer>>	predefinedSizes;

	/**
	 * The belonging values to the sizes
	 */
	private Map<String, Vector<String>>		predefinedValue;

	/**
	 * Constructs a new <code>RelationSizeData</code>
	 * 
	 * @param predefinedSizes
	 *           the defnied sizes
	 * @param predefinedValue
	 *           the values belonging to the sizes
	 */
	public RelationSizeData(Map<String, Vector<Integer>> predefinedSizes,
			Map<String, Vector<String>> predefinedValue)
	{
		this.predefinedSizes = predefinedSizes;
		this.predefinedValue = predefinedValue;
	}

	/**
	 * Returns the definied sizes
	 * 
	 * @return the definied sizes
	 */
	public Map<String, Vector<Integer>> getPredefinedSizes()
	{
		return predefinedSizes;
	}

	/**
	 * Sets the definied sizes
	 * 
	 * @param predefinedSizes
	 *           the definied sizes
	 */
	public void setPredefinedSizes(Map<String, Vector<Integer>> predefinedSizes)
	{
		this.predefinedSizes = predefinedSizes;
	}

	/**
	 * Returns the definied values
	 * 
	 * @return the definied values
	 */
	public Map<String, Vector<String>> getPredefinedValue()
	{
		return predefinedValue;
	}

	/**
	 * Sets the definied values
	 * 
	 * @param predefinedValue
	 *           the definied values
	 */
	public void setPredefinedValue(Map<String, Vector<String>> predefinedValue)
	{
		this.predefinedValue = predefinedValue;
	}
}
