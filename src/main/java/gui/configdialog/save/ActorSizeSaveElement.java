package gui.configdialog.save;

import data.AttributeType;

/**
 * This class contains save information for the CDialogActorSize dialog
 * 
 *
 */

public class ActorSizeSaveElement extends SaveElement
{
	private AttributeType aType;
	private Integer[] sizes;
	private int boxIndex;
	
	public ActorSizeSaveElement(AttributeType aType, Integer[] sizes, int boxIndex)
	{
		this.aType = aType;
		this.sizes = sizes;
		this.boxIndex = boxIndex;
	}

	/**
	 * @return the aType
	 */
	public AttributeType getType()
	{
		return aType;
	}

	/**
	 * @return the sizes
	 */
	public Integer[] getSizes()
	{
		return sizes;
	}
	
	/**
	 * 
	 * @return index of comboBox
	 */
	public int getBoxIndex()
	{
		return boxIndex;
	}
}
