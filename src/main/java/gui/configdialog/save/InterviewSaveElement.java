package gui.configdialog.save;

import interview.InterviewElementInformation;

import java.util.List;

public class InterviewSaveElement extends SaveElement
{
	private List<InterviewElementInformation> elementInformations;

	
	public InterviewSaveElement(
			List<InterviewElementInformation> elementInformations)
	{
		this.elementInformations = elementInformations;
	}

	/**
	 * @return the elementInformations
	 */
	public List<InterviewElementInformation> getElementInformations()
	{
		return elementInformations;
	}
	
}
